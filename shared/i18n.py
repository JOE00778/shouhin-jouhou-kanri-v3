def lang_selector():
    """主入口侧边栏调用一次。"""
    options = list(LANGS.keys())
    current = st.session_state.get("lang_label", "中文")
    idx = options.index(current) if current in options else 0
    selected = st.sidebar.selectbox(
        "🌐 Language / 言語",
        options,
        index=idx,
        key="lang_selector_widget",
    )
    st.session_state["lang_label"] = selected
    st.session_state["lang"] = LANGS[selected]
    return selected


def get_lang() -> str:
    """返回当前语言代码 zh / ja"""
    return st.session_state.get("lang", "zh")


def t(text: str) -> str:
    """翻译。lang=ja 查表，否则原样返回中文。
    未在表里的中文 key 也原样返回（fallback）。
    """
    if get_lang() == "ja":
        return TRANSLATIONS_JA.get(text, text)
    return text
