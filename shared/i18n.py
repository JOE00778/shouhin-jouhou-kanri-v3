def lang_selector():
    """每个 page 调用一次。
    - 隐藏 Streamlit 默认（基于文件名的）sidebar 导航
    - 在 sidebar 顶部渲染 🌐 切换器
    - 按三大块分组渲染可翻译导航（销售数据 / 订货决策 / 商品情报）
    """
    # 1) 隐藏默认导航
    st.markdown(
        "<style>[data-testid='stSidebarNav']{display:none!important;}</style>",
        unsafe_allow_html=True,
    )

    # 2) 语言切换器
    selected = st.sidebar.selectbox(
        "🌐 Language / 言語",
        list(LANGS.keys()),
        key="lang_label",
    )
    st.session_state["lang"] = LANGS[selected]

    # 3) 分组导航
    st.sidebar.divider()
    for group_label, pages in _NAV_GROUPS:
        if group_label is None:
            # 顶层直显（首页 / 设置）
            for path, label in pages:
                try:
                    st.sidebar.page_link(path, label=t(label))
                except Exception:
                    pass
        else:
            # 分组用 expander 包起来,默认展开
            with st.sidebar.expander(t(group_label), expanded=True):
                for path, label in pages:
                    try:
                        st.page_link(path, label=t(label))
                    except Exception:
                        pass

    return selected


def get_lang() -> str:
    """返回当前语言代码 zh / ja"""
    return st.session_state.get("lang", "zh")


def t(text: str) -> str:
    """翻译。lang=ja 查 TRANSLATIONS_JA;lang=zh 查 TRANSLATIONS_ZH(把日文 key 还原中文)。
    都查不到则原样返回。
    """
    if get_lang() == "ja":
        return TRANSLATIONS_JA.get(text, text)
    return TRANSLATIONS_ZH.get(text, text)
